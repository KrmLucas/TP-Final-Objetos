
public class Tamanio {

	private double ancho;
	private double alto;
	
	
	public Tamanio (double ancho, double alto){
		this.alto = alto;
		this.ancho = ancho;
	}

	public double getAncho() {
		return ancho;
	}

	public void setAncho(double ancho) {
		this.ancho = ancho;
	}

	public double getAlto() {
		return alto;
	}

	public void setAlto(double alto) {
		this.alto = alto;
	}
	
	
	
}

