
public class Tamanio {

	private int ancho;
	private int alto;
	
	
	public Tamanio (int ancho, int alto){
		this.alto = alto;
		this.ancho = ancho;
	}

	public int getAncho() {
		return ancho;
	}

	public void setAncho(int ancho) {
		this.ancho = ancho;
	}

	public int getAlto() {
		return alto;
	}

	public void setAlto(int alto) {
		this.alto = alto;
	}
	
	
	
}

